# Financial Value Extraction
# ---------------------------------------------------------------------------

def _extract_financial_values(
    model: FinancialModel, ts: dict, strategy: Strategy,
) -> dict:
    """Extract all financial values needed for the template."""
    per_student = ts.get("ts_per_student_budget", 25_000)
    pnl = model.pnl_projection
    y5_students = int(ts.get(
        "ts_students_year5",
        pnl[-1].students if pnl else 100_000,
    ))
    num_schools = int(ts.get(
        "ts_num_schools",
        pnl[-1].schools if pnl else 10,
    ))

    # Upfront breakdown ($M)
    def _upfront(ts_key: str, model_val: float, default: int) -> float:
        return ts.get(
            ts_key,
            round(model_val / 1_000_000) if model_val > 0
            else default,
        )

    upfront_alphacore = _upfront(
        "ts_upfront_alphacore_license",
        model.upfront_alphacore_license, 250,
    )
    upfront_incept_edllm = _upfront(
        "ts_upfront_incept_edllm",
        model.upfront_incept_edllm, 250,
    )
    upfront_app_rd = _upfront(
        "ts_upfront_app_content_rd",
        model.upfront_app_content_rd, 250,
    )
    upfront_lifeskills = _upfront(
        "ts_upfront_lifeskills_rd",
        model.upfront_lifeskills_rd, 250,
    )
    upfront_mgmt = _upfront(
        "ts_upfront_mgmt_fee", model.upfront_mgmt_fee, 250,
    )
    upfront_timeback = _upfront(
        "ts_upfront_timeback_fee", model.upfront_timeback_fee, 500,
    )
    upfront_total = ts.get(
        "ts_upfront_ip_fee",
        (
            round(model.upfront_ip_fee / 1_000_000)
            if model.upfront_ip_fee > 1_000_000
            else model.upfront_ip_fee
        ),
    )

    # IP costs (AlphaCore + Incept EdLLM + EdTech Apps + Programs & LifeSkills)
    ip_development_total = (
        upfront_alphacore + upfront_incept_edllm
        + upfront_app_rd + upfront_lifeskills
    )
    fee_prepays_total = upfront_mgmt + upfront_timeback

    mgmt_pct = int(ts.get(
        "ts_management_fee_pct",
        round(model.management_fee_pct * 100),
    ))
    timeback_pct = int(ts.get(
        "ts_timeback_license_pct",
        round(model.timeback_license_pct * 100),
    ))

    # Ongoing costs
    per_student_mgmt = round(per_student * mgmt_pct / 100)
    per_student_timeback = round(per_student * timeback_pct / 100)

    # Annual revenue at scale
    annual_revenue_at_scale = y5_students * per_student

    # Funding gap: $25K per student less current public funding
    current_public_spend = min(15_000, per_student * 0.6)
    funding_gap_per_student = per_student - current_public_spend
    funding_gap_annual_100k = round(
        100_000 * funding_gap_per_student / 1_000_000_000, 1,
    )

    # Parent Education / Launch / Guide Training: fixed $50M/yr per rules
    parent_education_annual = ts.get("ts_parent_education_annual", 50)
    launch_capital_annual = ts.get("ts_launch_capital", parent_education_annual)

    # Cumulative students for IP amortization
    cumulative_students = 0
    if pnl:
        cumulative_students = sum(p.students for p in pnl)
    ip_amort_per_student = (
        round(ip_development_total * 1_000_000 / max(1, cumulative_students))
        if cumulative_students else 0
    )

    # Scaling plan
    scaling_plan: list[dict] = []
    if strategy.phased_rollout:
        for ph in strategy.phased_rollout[:5]:
            scaling_plan.append({
                "phase": ph.phase or "",
                "timeline": ph.timeline or "",
                "students": ph.student_count or 0,
            })
    elif pnl:
        for p in pnl:
            scaling_plan.append({
                "phase": f"Year {p.year}",
                "timeline": f"SY{26 + p.year - 1}-{27 + p.year - 1}",
                "students": p.students,
            })

    # --- National student commitment (excludes flagship) ---
    national_students = int(
        model.national_students
        if model.national_students
        else 100_000
    )

    # --- Flagship data (from optimization result or assumptions) ---
    flagship_opt = model.flagship_optimization
    flagship_tuition = model.flagship_tuition or 0
    flagship_students_total = model.flagship_students or 0

    # Derive schools and per-school from optimization metros
    if flagship_opt and flagship_opt.total_schools > 0:
        flagship_schools = flagship_opt.total_schools
        flagship_per_school = flagship_opt.optimal_capacity
    else:
        # Fallback: use term-sheet assumptions → financial model defaults
        flagship_schools = int(ts.get("ts_num_flagship_schools", 3))
        if flagship_schools == 0:
            flagship_schools = 3  # Ensure we always show something
        flagship_per_school = (
            flagship_students_total // max(1, flagship_schools)
            if flagship_students_total and flagship_schools
            else 500  # Default from financial_rules_v1.md fallback
        )
        # Ensure tuition has a sensible fallback
        if flagship_tuition == 0:
            flagship_tuition = int(ts.get("ts_flagship_tuition", 65_000))

    return {
        "per_student": per_student,
        "y5_students": y5_students,
        "national_students": national_students,
        "num_schools": num_schools,
        "upfront_alphacore": upfront_alphacore,
        "upfront_incept_edllm": upfront_incept_edllm,
        "upfront_app_rd": upfront_app_rd,
        "upfront_lifeskills": upfront_lifeskills,
        "upfront_mgmt": upfront_mgmt,
        "upfront_timeback": upfront_timeback,
        "upfront_total": upfront_total,
        "ip_development_total": ip_development_total,
        "fee_prepays_total": fee_prepays_total,
        "mgmt_pct": mgmt_pct,
        "timeback_pct": timeback_pct,
        "per_student_mgmt": per_student_mgmt,
        "per_student_timeback": per_student_timeback,
        "annual_revenue_at_scale": annual_revenue_at_scale,
        "funding_gap_per_student": funding_gap_per_student,
        "funding_gap_annual_100k": funding_gap_annual_100k,
        "current_public_spend": current_public_spend,
        "parent_education_annual": parent_education_annual,
        "launch_capital_annual": launch_capital_annual,
        "ip_amort_per_student": ip_amort_per_student,
        "cumulative_students": cumulative_students,
        "scaling_plan": scaling_plan,
        "pnl": model.pnl_projection or [],
        # Flagship data
        "flagship_tuition": flagship_tuition,
        "flagship_students_total": flagship_students_total,
        "flagship_schools": flagship_schools,
        "flagship_per_school": flagship_per_school,
        "flagship_optimization": flagship_opt,
    }


