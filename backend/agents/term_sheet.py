def _extract_financial_values(
    model: FinancialModel, ts: dict, strategy: Strategy
) -> dict:
    """Extract all financial values needed for the template."""

    per_student = ts.get("ts_per_student_budget", 25_000)
    y5_students = int(ts.get("ts_students_year5",
                              model.pnl_projection[-1].students if model.pnl_projection else 100_000))
    num_schools = int(ts.get("ts_num_schools", model.pnl_projection[-1].schools if model.pnl_projection else 10))

    # Upfront breakdown ($M)
    upfront_alphacore = ts.get("ts_upfront_alphacore_license",
                                round(model.upfront_alphacore_license / 1_000_000) if model.upfront_alphacore_license > 0 else 250)
    upfront_app_rd = ts.get("ts_upfront_app_content_rd",
                             round(model.upfront_app_content_rd / 1_000_000) if model.upfront_app_content_rd > 0 else 250)
    upfront_lifeskills = ts.get("ts_upfront_lifeskills_rd",
                                 round(model.upfront_lifeskills_rd / 1_000_000) if model.upfront_lifeskills_rd > 0 else 250)
    upfront_mgmt = ts.get("ts_upfront_mgmt_fee",
                           round(model.upfront_mgmt_fee / 1_000_000) if model.upfront_mgmt_fee > 0 else 0)
    upfront_timeback = ts.get("ts_upfront_timeback_fee",
                               round(model.upfront_timeback_fee / 1_000_000) if model.upfront_timeback_fee > 0 else 0)
    upfront_total = ts.get("ts_upfront_ip_fee",
                            round(model.upfront_ip_fee / 1_000_000) if model.upfront_ip_fee > 1_000_000 else model.upfront_ip_fee)

    # IP costs for development items (AlphaCore + App R&D + LifeSkills R&D)
    ip_development_total = upfront_alphacore + upfront_app_rd + upfront_lifeskills
    # Fee prepays (management + timeback)
    fee_prepays_total = upfront_mgmt + upfront_timeback

    mgmt_pct = int(ts.get("ts_management_fee_pct", round(model.management_fee_pct * 100)))
    timeback_pct = int(ts.get("ts_timeback_license_pct", round(model.timeback_license_pct * 100)))

    # Ongoing costs
    per_student_mgmt = round(per_student * mgmt_pct / 100)
    per_student_timeback = round(per_student * timeback_pct / 100)

    # Annual revenue at scale
    annual_revenue_at_scale = y5_students * per_student
    # Funding gap (incremental above current public spend ~$15k baseline)
    current_public_spend = min(15_000, per_student * 0.6)
    funding_gap_per_student = per_student - current_public_spend
    funding_gap_annual_100k = round(100_000 * funding_gap_per_student / 1_000_000_000, 1)

    # Launch/marketing capital
    launch_capital_annual = ts.get("ts_launch_capital", 25)

    # Cumulative students for IP amortization
    cumulative_students = 0
    if model.pnl_projection:
        cumulative_students = sum(p.students for p in model.pnl_projection)
    ip_amort_per_student = round(ip_development_total * 1_000_000 / max(1, cumulative_students)) if cumulative_students else 0

    # Scaling plan
    scaling_plan = []
    if strategy.phased_rollout:
        for ph in strategy.phased_rollout[:5]:
            scaling_plan.append({
                "phase": ph.phase or "",
                "timeline": ph.timeline or "",
                "students": ph.student_count or 0,
            })
    elif model.pnl_projection:
        # Build from P&L projection
        for p in model.pnl_projection:
            scaling_plan.append({
                "phase": f"Year {p.year}",
                "timeline": f"SY{26 + p.year - 1}-{27 + p.year - 1}",
                "students": p.students,
            })

    return {
        "per_student": per_student,
        "y5_students": y5_students,
        "num_schools": num_schools,
        "upfront_alphacore": upfront_alphacore,
        "upfront_app_rd": upfront_app_rd,
        "upfront_lifeskills": upfront_lifeskills,
        "upfront_mgmt": upfront_mgmt,
        "upfront_timeback": upfront_timeback,
        "upfront_total": upfront_total,
        "ip_development_total": ip_development_total,
        "fee_prepays_total": fee_prepays_total,
        "mgmt_pct": mgmt_pct,
        "timeback_pct": timeback_pct,
        "per_student_mgmt": per_student_mgmt,
        "per_student_timeback": per_student_timeback,
        "annual_revenue_at_scale": annual_revenue_at_scale,
        "funding_gap_annual_100k": funding_gap_annual_100k,
        "launch_capital_annual": launch_capital_annual,
        "ip_amort_per_student": ip_amort_per_student,
        "cumulative_students": cumulative_students,
        "scaling_plan": scaling_plan,
        "pnl": model.pnl_projection or [],
    }


