def _add_flagship_summary_table(
    doc: DocxDocument, cv: CountryVariables,
    fin: dict, model: FinancialModel,
) -> None:
    """Add the Alpha Flagship Schools summary table (Table 3).

    Uses the optimization result when available — shows real metro names,
    data-driven school counts, capacity, and tuition from the grid search.
    Falls back to synthetic rows when no optimization data exists.
    """
    flagship_opt = fin.get("flagship_optimization")
    flagship_tuition = fin.get("flagship_tuition", 0)
    flagship_schools = fin.get("flagship_schools", 0)
    flagship_per_school = fin.get("flagship_per_school", 0)

    if flagship_tuition == 0 or flagship_schools == 0:
        _add_body(
            doc,
            "Alpha Flagship School details to be determined based on "
            "metro-level AGI analysis.",
        )
        return

    headers = [
        "Metro",
        "Schools",
        "Students / School",
        "Tuition / Year",
        "Annual Revenue",
    ]

    rows: list[list[str]] = []
    total_schools = 0
    total_students = 0
    total_revenue = 0

    # --- Build metro rows from optimization result ---
    if flagship_opt and flagship_opt.metros:
        for mr in flagship_opt.metros:
            metro_students = mr.schools * mr.capacity_per_school
            total_schools += mr.schools
            total_students += metro_students
            total_revenue += mr.annual_revenue
            rows.append([
                mr.metro_name,
                str(mr.schools),
                f"{mr.capacity_per_school:,}",
                f"${mr.tuition:,.0f}",
                f"${mr.annual_revenue / 1_000_000:.0f}M",
            ])
    else:
        # Fallback: distribute schools across capital + other metros
        capital_schools = min(3, flagship_schools)
        remaining = flagship_schools - capital_schools

        _fallback_metros = [
            (cv.first_launch_city or "Capital City", capital_schools),
        ]
        if remaining >= 1 and cv.second_city:
            _fallback_metros.append((cv.second_city, 1))
            remaining -= 1
        if remaining >= 1:
            _fallback_metros.append(("Third Metro", remaining))

        for metro_name, n_schools in _fallback_metros:
            metro_students = n_schools * flagship_per_school
            rev = metro_students * flagship_tuition
            total_schools += n_schools
            total_students += metro_students
            total_revenue += rev
            rows.append([
                metro_name,
                str(n_schools),
                f"{flagship_per_school:,}",
                f"${flagship_tuition:,.0f}",
                f"${rev / 1_000_000:.0f}M",
            ])

    # Total row — show tuition only if uniform across metros
    tuition_values = [mr.tuition for mr in flagship_opt.metros] if (
        flagship_opt and flagship_opt.metros
    ) else []
    if tuition_values and len(set(tuition_values)) == 1:
        total_tuition_str = f"${tuition_values[0]:,.0f}"
    elif tuition_values:
        total_tuition_str = (
            f"${min(tuition_values):,.0f} – "
            f"${max(tuition_values):,.0f}"
        )
    else:
        total_tuition_str = f"${flagship_tuition:,.0f}"
    rows.append([
        "TOTAL",
        str(total_schools),
        f"{total_students:,} total",
        total_tuition_str,
        f"${total_revenue / 1_000_000:.0f}M",
    ])

    _add_styled_table(doc, headers, rows, bold_last_row=True)


# ---------------------------------------------------------------------------
# Table Rendering Helper
# ---------------------------------------------------------------------------

def _add_styled_table(
    doc: DocxDocument,
    headers: list[str],
    rows: list[list[str]],
    bold_last_row: bool = False,
) -> None:
    """Add a professionally styled table to the document.

    Matches the benchmark format: header row with accent background,
    alternating row shading, clean borders.
    """
    num_cols = len(headers)
    num_rows = len(rows) + 1  # +1 for header

    table = doc.add_table(rows=num_rows, cols=num_cols)
    table.style = "Table Grid"
    table.alignment = WD_TABLE_ALIGNMENT.CENTER

    # Prevent rows from breaking across pages and keep table together
    total_rows = len(table.rows)
    for row_idx, row in enumerate(table.rows):
        tr = row._tr
        tr_pr = tr.get_or_add_trPr()
        tr_pr.append(tr_pr.makeelement(qn("w:cantSplit"), {}))
        is_last_row = row_idx == total_rows - 1
        for cell in row.cells:
            for p in cell.paragraphs:
                p.paragraph_format.keep_together = True
                if not is_last_row:
                    p.paragraph_format.keep_with_next = True

    # --- Header row ---
    for ci, header_text in enumerate(headers):
        cell = table.rows[0].cells[ci]
        paras = cell.paragraphs
        p = paras[0] if paras else cell.add_paragraph()
        p.paragraph_format.space_before = DocxPt(2)
        p.paragraph_format.space_after = DocxPt(2)
        run = p.add_run(header_text)
        run.bold = True
        run.font.name = "Calibri"
        run.font.size = DocxPt(9)
        run.font.color.rgb = DocxRGB(0xFF, 0xFF, 0xFF)
        # Accent background
        tc = cell._element
        tc_pr = tc.get_or_add_tcPr()
        shading_el = tc_pr.makeelement(qn("w:shd"), {
            qn("w:fill"): "006D77",
            qn("w:val"): "clear",
        })
        tc_pr.append(shading_el)

    # --- Data rows ---
    for ri, row_data in enumerate(rows):
        is_separator = (
            bool(row_data)
            and len(set(row_data)) == 1
            and len(row_data[0]) > 20
        )
        is_last = (ri == len(rows) - 1)

        if is_separator:
            # Merge all cells into one separator row
            first_cell = table.rows[ri + 1].cells[0]
            last_cell = table.rows[ri + 1].cells[num_cols - 1]
            merged = first_cell.merge(last_cell)
            paras = merged.paragraphs
            p = paras[0] if paras else merged.add_paragraph()
            p.paragraph_format.space_before = DocxPt(2)
            p.paragraph_format.space_after = DocxPt(2)
            run = p.add_run(row_data[0])
            run.font.name = "Calibri"
            run.font.size = DocxPt(8)
            run.font.color.rgb = DocxRGB(0x66, 0x66, 0x66)
            run.italic = True
            # Light background
            tc = merged._element
            tc_pr = tc.get_or_add_tcPr()
            shading_el = tc_pr.makeelement(qn("w:shd"), {
                qn("w:fill"): "E8E8E8",
                qn("w:val"): "clear",
            })
            tc_pr.append(shading_el)
            continue

        for ci in range(num_cols):
            cell_text = (
                row_data[ci] if ci < len(row_data) else ""
            )
            cell = table.rows[ri + 1].cells[ci]
            paras = cell.paragraphs
            p = paras[0] if paras else cell.add_paragraph()
            p.paragraph_format.space_before = DocxPt(2)
            p.paragraph_format.space_after = DocxPt(2)

            is_bold = (
                (bold_last_row and is_last)
                or cell_text.startswith("**")
            )
            display_text = (
                cell_text.strip("*")
                if cell_text.startswith("**") else cell_text
            )
            run = p.add_run(display_text)
            run.font.name = "Calibri"
            run.font.size = DocxPt(9)
            run.font.color.rgb = DocxRGB(0x33, 0x33, 0x33)
            run.bold = is_bold

            # Alternating row shading
            if ri % 2 == 1:
                tc = cell._element
                tc_pr = tc.get_or_add_tcPr()
                shading_el = tc_pr.makeelement(qn("w:shd"), {
                    qn("w:fill"): "F5F5F5",
                    qn("w:val"): "clear",
                })
                tc_pr.append(shading_el)

    doc.add_paragraph("")  # Spacer after table


# ---------------------------------------------------------------------------
# Paragraph / List Item Helpers
# ---------------------------------------------------------------------------

def _add_body(doc: DocxDocument, text: str) -> None:
    """Add a normal body paragraph."""
    p = doc.add_paragraph(text)
    p.style = doc.styles["Normal"]


def _add_list_item(doc: DocxDocument, text: str) -> None:
    """Add a bullet list item."""
    doc.add_paragraph(text, style="List Bullet")


def _add_list_item_bold_prefix(
    doc: DocxDocument, bold_part: str, rest: str,
) -> None:
    """Add a bullet with bold prefix then normal text.

    e.g. "**Timeback:** the AI platform..."
    """
    p = doc.add_paragraph(style="List Bullet")
    run_bold = p.add_run(bold_part)
    run_bold.bold = True
    run_bold.font.name = "Calibri"
    run_bold.font.size = DocxPt(11)
    p.add_run(rest)


