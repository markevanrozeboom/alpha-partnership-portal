async def generate_term_sheet(
    target: str,
    country_profile: CountryProfile,
    education_analysis: EducationAnalysis,
    strategy: Strategy,
    financial_model: FinancialModel,
    assumptions: FinancialAssumptions,
    term_sheet_assumptions: FinancialAssumptions | None = None,
) -> tuple[str, str]:
    """Generate a combined proposal document (term sheet + deal terms).

    Returns (summary_markdown, proposal_docx_path).
    The DOCX is the primary deliverable — a professionally formatted document
    matching the Alpha_Ed71_Combined_Proposal benchmark.
    """
    logger.info("Generating combined proposal for %s", target)

    # --- Extract deal parameters from term sheet assumptions ---
    ts = {}
    if term_sheet_assumptions and term_sheet_assumptions.assumptions:
        ts = {item.key: item.value for item in term_sheet_assumptions.assumptions}

    per_student = ts.get("ts_per_student_budget", 25_000)
    y5_students = int(ts.get("ts_students_year5",
                              financial_model.pnl_projection[-1].students if financial_model.pnl_projection else 100_000))

    # --- Generate country-specific variables ---
    cv = await _generate_country_variables(
        target, country_profile, education_analysis, strategy,
        financial_model, per_student, y5_students,
    )

    # --- Build the DOCX ---
    docx_path = _build_proposal_docx(
        target=target,
        cv=cv,
        financial_model=financial_model,
        assumptions=assumptions,
        ts=ts,
        strategy=strategy,
        country_profile=country_profile,
    )

    # --- Build a simple summary markdown (for frontend display if needed) ---
    summary_md = _build_summary_markdown(target, cv, ts, financial_model)

    logger.info("Combined proposal generated for %s", target)
    return summary_md, docx_path


# ---------------------------------------------------------------------------
# DOCX Builder — template-based, matching benchmark format
# ---------------------------------------------------------------------------

def _build_proposal_docx(
    target: str,
    cv: CountryVariables,
    financial_model: FinancialModel,
    assumptions: FinancialAssumptions,
    ts: dict,
    strategy: Strategy,
    country_profile: CountryProfile,
) -> str:
    """Build the full combined proposal DOCX matching the Alpha_Ed71 benchmark.

    Part 1 (pages 1-5): Strategic partnership proposal narrative
    Part 2 (pages 6-7): Deal terms and financial tables
    """
    doc = DocxDocument()

    # --- Page setup ---
    for section in doc.sections:
        section.top_margin = Cm(2.54)
        section.bottom_margin = Cm(2.54)
        section.left_margin = Cm(2.54)
        section.right_margin = Cm(2.54)

    # --- Configure styles ---
    _configure_styles(doc)

    # --- Extract key financial values ---
    fin = _extract_financial_values(financial_model, ts, strategy)

    # ===================================================================
    # PART 1: COMBINED PROPOSAL (Pages 1-5)
    # ===================================================================

    # Confidential header
    _add_confidential_header(doc, target, cv)

    # Section 1: Overview
    _add_overview_section(doc, target, cv, fin)

    # Section 2: Transformation Steps and Alpha's Proven Capabilities
    _add_transformation_section(doc, target, cv, fin)

    # Section 3: The Newly Created Cultural Program and Education Assets
    _add_cultural_program_section(doc, target, cv, fin)

    # Section 4: School Network
    _add_school_network_section(doc, target, cv, fin, strategy)

    # Section 5: Future Alpha and Regional Opportunities
    _add_future_opportunities_section(doc, target, cv)

    # Section 6: High-level Sequencing Plan
    _add_sequencing_section(doc, target, cv, fin, strategy)

    # Section 7: Funding, Investment, and Real Estate
    _add_funding_section(doc, target, cv, fin)

    # ===================================================================
    # PART 2: DEAL TERMS (Pages 6-7)
    # ===================================================================

    doc.add_page_break()

    # Confidential header (repeated for Part 2)
    _add_confidential_header(doc, target, cv)

