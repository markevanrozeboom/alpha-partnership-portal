# Summary Markdown (for frontend display)
# ---------------------------------------------------------------------------

def _build_summary_markdown(
    target: str, cv: CountryVariables,
    ts: dict, model: FinancialModel,
) -> str:
    """Build a summary markdown string for frontend display."""
    per_student = ts.get("ts_per_student_budget", 25_000)
    pnl = model.pnl_projection
    y5_students = int(ts.get(
        "ts_students_year5",
        pnl[-1].students if pnl else 100_000,
    ))
    upfront = ts.get(
        "ts_upfront_ip_fee",
        (
            round(model.upfront_ip_fee / 1_000_000)
            if model.upfront_ip_fee > 1_000_000
            else model.upfront_ip_fee
        ),
    )
    mgmt_fee = ts.get("ts_management_fee_pct", 10)
    tb_fee = ts.get("ts_timeback_license_pct", 20)

    lines = [
        f"# {cv.jv_program_name} \u2014 Combined Proposal",
        f"## Alpha Holdings, Inc. \u00d7 {target}",
        "",
        f"**Program Name:** {cv.jv_program_name}",
        f"**Cultural Program:** {cv.cultural_program_name}",
        f"**Credential:** {cv.credential_phrase}",
        "",
        "### Key Terms",
        f"- **Upfront Ask:** ${upfront:,.0f}M",
        f"- **Per-Student Budget:** ${per_student:,.0f}",
        f"- **Year 5 Students:** {y5_students:,}",
        f"- **Management Fee:** {mgmt_fee}%",
        f"- **Timeback License:** {tb_fee}%",
        "",
        "### Scaling Plan",
    ]

    if pnl:
        for p in pnl:
            lines.append(
                f"- Year {p.year}: {p.students:,} students | "
                f"{p.schools} schools | ${p.revenue:,.0f} revenue"
            )

    lines.extend([
        "",
        "*This proposal document is confidential and non-binding. "
        "All terms are subject to final negotiation and execution "
        "of definitive agreements.*",
    ])

    return "\n".join(lines)
