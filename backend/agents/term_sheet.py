"""Term Sheet Generator — produces a professional combined proposal DOCX.

Modelled on the Alpha_Ed71_Combined_Proposal.docx benchmark document:
  Part 1 (pages 1-5): Strategic partnership proposal narrative
  Part 2 (pages 6-7): Deal terms and financial tables

~70% of the document is boilerplate matching the UAE benchmark.
~30% is country/state-specific content generated by the LLM.

Includes a HITL gate for term sheet assumptions (deal terms)
that the user can review/adjust before the term sheet is generated.
Changes to key financial terms trigger financial model recalculation.
"""

