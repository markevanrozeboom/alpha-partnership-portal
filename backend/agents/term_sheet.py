# ---------------------------------------------------------------------------
# Table Rendering Helper
# ---------------------------------------------------------------------------

def _add_styled_table(
    doc: DocxDocument,
    headers: list[str],
    rows: list[list[str]],
    bold_last_row: bool = False,
) -> None:
    """Add a professionally styled table to the document.

    Matches the benchmark format: header row with accent background,
    alternating row shading, clean borders.
    """
    num_cols = len(headers)
    num_rows = len(rows) + 1  # +1 for header

    table = doc.add_table(rows=num_rows, cols=num_cols)
    table.style = "Table Grid"
    table.alignment = WD_TABLE_ALIGNMENT.CENTER

    # --- Header row ---
    for ci, header_text in enumerate(headers):
        cell = table.rows[0].cells[ci]
        p = cell.paragraphs[0] if cell.paragraphs else cell.add_paragraph()
        p.paragraph_format.space_before = DocxPt(2)
        p.paragraph_format.space_after = DocxPt(2)
        run = p.add_run(header_text)
        run.bold = True
        run.font.name = "Calibri"
        run.font.size = DocxPt(9)
        run.font.color.rgb = DocxRGB(0xFF, 0xFF, 0xFF)
        # Accent background
        tc = cell._element
        tc_pr = tc.get_or_add_tcPr()
        shading_el = tc_pr.makeelement(qn("w:shd"), {
            qn("w:fill"): "006D77",
            qn("w:val"): "clear",
        })
        tc_pr.append(shading_el)

    # --- Data rows ---
    for ri, row_data in enumerate(rows):
        is_separator = (
            bool(row_data)
            and len(set(row_data)) == 1
            and len(row_data[0]) > 20
        )

        is_last = (ri == len(rows) - 1)

        if is_separator:
            # Merge all cells into one spanning separator row
            first_cell = table.rows[ri + 1].cells[0]
            last_cell = table.rows[ri + 1].cells[num_cols - 1]
            merged = first_cell.merge(last_cell)
            p = merged.paragraphs[0] if merged.paragraphs else merged.add_paragraph()
            p.paragraph_format.space_before = DocxPt(2)
            p.paragraph_format.space_after = DocxPt(2)
            run = p.add_run(row_data[0])
            run.font.name = "Calibri"
            run.font.size = DocxPt(8)
            run.font.color.rgb = DocxRGB(0x66, 0x66, 0x66)
            run.italic = True
            # Light background
            tc = merged._element
            tc_pr = tc.get_or_add_tcPr()
            shading_el = tc_pr.makeelement(qn("w:shd"), {
                qn("w:fill"): "E8E8E8",
                qn("w:val"): "clear",
            })
            tc_pr.append(shading_el)
            continue

        for ci in range(num_cols):
            cell_text = row_data[ci] if ci < len(row_data) else ""
            cell = table.rows[ri + 1].cells[ci]
            p = cell.paragraphs[0] if cell.paragraphs else cell.add_paragraph()
            p.paragraph_format.space_before = DocxPt(2)
            p.paragraph_format.space_after = DocxPt(2)

            is_bold = (bold_last_row and is_last) or cell_text.startswith("**")
            display_text = cell_text.strip("*") if cell_text.startswith("**") else cell_text
            run = p.add_run(display_text)
            run.font.name = "Calibri"
            run.font.size = DocxPt(9)
            run.font.color.rgb = DocxRGB(0x33, 0x33, 0x33)
            run.bold = is_bold

            # Alternating row shading
            if ri % 2 == 1:
                    tc = cell._element
                    tc_pr = tc.get_or_add_tcPr()
                    shading_el = tc_pr.makeelement(qn("w:shd"), {
                    qn("w:fill"): "F5F5F5",
                        qn("w:val"): "clear",
                    })
                    tc_pr.append(shading_el)

    doc.add_paragraph("")  # Spacer after table


