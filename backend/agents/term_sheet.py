class CountryVariables(BaseModel):
    """Country-specific content that gets inserted into the proposal template.

    ~30% of the proposal document varies by country. This model captures
    all the country-specific text variables needed for template substitution.
    """
    country_adjective: str = Field(description="National adjective, e.g. 'French', 'Saudi', 'Brazilian'")
    jv_program_name: str = Field(description="Partnership program name (equivalent of 'Ed71' for UAE). Short, memorable, 2-4 words max. MUST use only plain ASCII characters (no accents, no diacritics). Example: 'Ed71', 'Education 2030', 'Alpha France' — NOT 'Éducation 2030'.")
    cultural_program_name: str = Field(description="Country's life-skills curriculum program name (equivalent of 'AsasCore' for UAE). 1-2 words, culturally resonant. MUST use only plain ASCII characters (no accents, no diacritics). Example: 'AsasCore', 'Savoir' — NOT 'Éducation'.")
    credential_phrase: str = Field(description="Prestige education credential phrase, e.g. 'Educated in the Emirates'. Format: 'Educated in [Country]'")
    first_launch_city: str = Field(description="Best city to launch first schools (capital or major education hub)")
    second_city: str = Field(description="Second city for expansion")
    language_subjects: str = Field(description="Country-specific education app subjects needing localization (equivalent of 'Arabic, Islamic Studies, Social Studies, ESL for Arabic speakers'). Comma-separated list of 4-5 subjects.")
    edu_llm_training: str = Field(description="What the country-specific eduLLM would be trained on (equivalent of 'UAE language, laws, customs, culture, and values'). Short phrase.")
    cultural_values: str = Field(description="What the cultural program would encode (equivalent of 'Emirati values, habits, and aspirations'). Short phrase.")
    ministry_reference: str = Field(description="Name of the relevant education authority/ministry")
    regulatory_framework: str = Field(description="How schools would operate under local law (equivalent of 'ADEK / Ministry frameworks')")
    funding_mechanism: str = Field(description="How student funding would work in this country (1-2 sentences)")
    national_credential_reference: str = Field(description="A prestige local institution for comparison (equivalent of 'NYU Abu Dhabi')")
    country_specific_overview: str = Field(description="2-3 sentences about why this country is ripe for education transformation. Reference specific national plans, reforms, or challenges.")
    school_infrastructure_note: str = Field(description="1 sentence about how schools will integrate with existing infrastructure")


COUNTRY_VARIABLES_PROMPT = """You are preparing a strategic education partnership proposal between
Alpha Holdings / 2hr Learning and {target}. This proposal will be presented to the head of state
and senior government decision-makers.

You need to generate country-specific content variables that will be inserted into a standardized
proposal template (modelled on the UAE Ed71 deal structure).

Every value must be culturally appropriate, factually accurate, and diplomatically calibrated for
a head-of-state audience. The program names should sound local, prestigious, and aspirational.

Country Context:
- Population: {population}
- K-12 Students: {k12_students}
- GDP per capita: ${gdp_per_capita}
- Education ministry: {ministry}
- Head of state: {head_of_state}
- National vision/plan: {national_vision}
- Government type: {government_type}
- Per-student budget for this deal: ${per_student_budget}
- Year 5 student target: {y5_students}

Education Context:
{education_context}

Strategy Context:
{strategy_context}

Generate the country-specific variables for the proposal template."""


def _strip_accents(text: str) -> str:
    """Strip accents/diacritics from text, keeping base ASCII characters.

    Prevents 'Éducation' → 'E?ducation' in the PDF renderer.
    """
    nfkd = unicodedata.normalize("NFKD", text)
    return "".join(c for c in nfkd if not unicodedata.combining(c))


def _sanitize_country_variables(cv: CountryVariables) -> CountryVariables:
    """Ensure all program/brand names are ASCII-safe for PDF rendering."""
    cv.jv_program_name = _strip_accents(cv.jv_program_name)
    cv.cultural_program_name = _strip_accents(cv.cultural_program_name)
    cv.credential_phrase = _strip_accents(cv.credential_phrase)
    return cv


async def _generate_country_variables(
    target: str,
    country_profile: CountryProfile,
    education_analysis: EducationAnalysis,
    strategy: Strategy,
    financial_model: FinancialModel,
    per_student_budget: float,
    y5_students: int,
) -> CountryVariables:
    """Generate country-specific variables via LLM structured output."""

    # Build education context
    edu_parts = []
    if education_analysis.system_diagnosis.primary_pain_points:
        edu_parts.append("Student pain points: " + "; ".join(education_analysis.system_diagnosis.primary_pain_points[:5]))
    if education_analysis.system_diagnosis.parent_pain_points:
        edu_parts.append("Parent pain points: " + "; ".join(education_analysis.system_diagnosis.parent_pain_points[:5]))
    if education_analysis.reform_landscape.active_reforms:
        edu_parts.append("Active reforms: " + "; ".join(education_analysis.reform_landscape.active_reforms[:5]))
    if education_analysis.reform_landscape.appetite_for_foreign_models:
        edu_parts.append(f"Appetite for foreign models: {education_analysis.reform_landscape.appetite_for_foreign_models}")

    # Build strategy context
    strat_parts = []
    if strategy.entry_mode:
        strat_parts.append(f"Entry mode: {strategy.entry_mode.value}")
    if strategy.partnership_structure.type:
        strat_parts.append(f"Partnership: {strategy.partnership_structure.type.value}")
    if strategy.brand.jv_name_suggestion:
        strat_parts.append(f"Suggested JV name: {strategy.brand.jv_name_suggestion}")
    if strategy.phased_rollout:
        for ph in strategy.phased_rollout[:3]:
            strat_parts.append(f"Phase: {ph.phase} ({ph.timeline}) - {ph.student_count or 'TBD'} students")

    prompt = COUNTRY_VARIABLES_PROMPT.format(
        target=target,
        population=f"{country_profile.demographics.total_population:,.0f}" if country_profile.demographics.total_population else "N/A",
        k12_students=f"{country_profile.education.k12_enrolled:,.0f}" if country_profile.education.k12_enrolled else "N/A",
        gdp_per_capita=f"{country_profile.economy.gdp_per_capita:,.0f}" if country_profile.economy.gdp_per_capita else "N/A",
        ministry=country_profile.regulatory.ministry_of_education or "N/A",
        head_of_state=country_profile.political_context.head_of_state or "N/A",
        national_vision=country_profile.political_context.national_vision_plan or "N/A",
        government_type=country_profile.political_context.government_type or "N/A",
        per_student_budget=f"{per_student_budget:,.0f}",
        y5_students=f"{y5_students:,}",
        education_context="\n".join(edu_parts) or "N/A",
        strategy_context="\n".join(strat_parts) or "N/A",
    )

    try:
        result = await call_llm(
            system_prompt=prompt,
            user_prompt=f"Generate the country-specific variables for {target}.",
            output_schema=CountryVariables,
        )
        return _sanitize_country_variables(result)  # type: ignore[return-value]
    except Exception as exc:
        logger.error("Failed to generate country variables for %s: %s", target, exc)
        # Fallback with sensible defaults
        return CountryVariables(
            country_adjective=target.split()[-1] if " " in target else target,
            jv_program_name=f"Alpha {target}",
            cultural_program_name=f"{target}Core",
            credential_phrase=f"Educated in {target}",
            first_launch_city="the capital",
            second_city="major cities",
            language_subjects=f"National language, Social Studies, Civic Education, History",
            edu_llm_training=f"{target} language, laws, customs, culture, and values",
            cultural_values=f"{target} values, traditions, and aspirations",
            ministry_reference="Ministry of Education",
            regulatory_framework="national education regulatory framework",
            funding_mechanism="Government funding through public education budgets",
            national_credential_reference="leading national university",
            country_specific_overview=f"{target} presents a significant opportunity for education transformation.",
            school_infrastructure_note=f"Schools will integrate with {target}'s existing educational infrastructure.",
        )


